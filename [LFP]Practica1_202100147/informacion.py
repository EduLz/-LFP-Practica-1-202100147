class informacion:
    def __init__(self, nombrePelicula, actores, anio, genero):
        self.nombrePelicula = nombrePelicula
        self.actores = actores
        self.anio = anio
        self.genero = genero
    def print_nombrePelicula(self):
        print(self.nombrePelicula)
    def print_actores(self):
        print(self.actores)
    def print_anio(self):
        print(self.anio) 
    def print_genero(self):
        print(self.genero)
                 